package week1.day1;

public class FindOddOrEvenNumber {

	public static void main(String[] args) {
		int userInput = 2;
		
		if(userInput % 2 == 0)
			System.out.println("The input number" + " "+ userInput + " " +"is even");
		else 
			System.out.println("The input number" + " " + userInput + " " + "is odd");
				
				
	}
}
